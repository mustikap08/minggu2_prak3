            <h3><li>Kantin</h3>
            Kantin merupakan salah satu tempat bagi anak Jurusan Teknologi Informasi untuk membeli makanan dan 
            minuman pada jam istirahat. Kantin ini menyediakan berbagai macam makanan mulai dari snack ringan, 
            nasi jagung, nasi kepal, roti hingga mie gelas. Tak hanya makanan, kantin ini juga menyediakan berbagai 
            macam minuman mulai dari yang bersoda, susu, yogurt, kopi dingin, hingga kopi panas yang dibuat sendiri 
            oleh para mahasiswa atau dosen.Harga yang ada pada kantin JTI ini juga tidak terlalu mahal, semua makanannya 
            berkisar dari harga 2.000 hingga 12.000. Untuk harga minumannya pun juga tak jauh berbeda dengan harga makanan. 
            Kantin in terletak di lobby Gedung Sipil lantai 7.
            </li>
            <h3><li>Ruang Baca</h3>
            Ruang baca Jurusan Teknologi Informasi merupakan tempat untuk mahasiswa Jurusan Teknologi Informasi membaca 
            maupun melakakukan aktivitas seperti kerja kelompok. Ruang baca ini terletak di Gedung AF lantai 3. Fasilitas 
            yang ada pada ruangan ini terbilang cukup lengkap, diantaranya : 2 ac demi menunjang kenyamanan mahasiswa, 
            meja kecil, meja besar, kursi, loker, serta berbagai buku dengan judul dan materi yang bias menunjang perkuliahan.
            Lantai yang terbuat dari kayu juga menjadi salah satu penunjang kenyamanan mahasiswa yang berada didalamnya. 
            Serta ruangan yang bersih dan wangi yang menjadikan mahasiswa semangat untuk belajar.
            </li>
            <h3><li>Ruang Admin</h3>
            Ruang ini adalah ruangan yang digunakan untuk tempat administrasi Jurusan untuk prodi D4-TI dan juga D3-MI. 
            Ruangan ini berada pada lantai 2 gedung AF, dekat dengan mushola gedung. Disini adalah tempat para mahasiswa 
            untuk mengambil absensi, memberikan surat sakit, mengurus surat dispensasi, dan juga mengurus kompensasi alpha. 
            Seluruh kegiatan administrasi jurusan berada diruangan ini, beberapa dosen juga menempati ruangan ini, dan juga 
            KPS berada pada ruangan ini agar lebih mudah menghubungi KPS saat adanya keperluan administrasi. Sama dengan 
            semua ruangan yang ada pada JTI ruangan ini dilengkapi dengan AC dan juga CCTV untuk memantau segala kegiatan 
            yang ada dalam ruang administrasi Jurusan ini.
            </li>
            <h3><li>Mushola</h3>
            Mushola yang terletak di Gedung AF lantai 2 ini biasanya digunakan untuk dosen dan mahasiswa Jurusan Teknilogi 
            Informasi untuk beribadah, khususnya mahasiswa atau dosen yang beragama islam. Fasilitas yang ada pada mushola 
            ini diantara : beberapa sajadah, mukena, sarung, Al-Quran, rak, serta tasbih. Tak lupa terdapat sandal yang bias 
            digunakan untuk mahasiswa maupun dosen untuk berwudhu.Mushola ini tak hanya bias digunakan untuk mahasiswa dan 
            dosen saja, namun tamu juga bias menggunakan mushola pada Gedung AF lantai 2 ini.
            </li>
            <h3><li>Lobby</h3>
            Disini terdapat Mading yang digunakan untuk menyebarkan informasi berupa poster yang dapat ditempelkan didalam mading. 
            Disini juga terdapat beberapa tempat duduk untuk para pegunjung gedung beristirahat sejenak, lalu terdapat pula petunjuk 
            arah apabila pengunjung tidak mengetahui arah kemana untuk mencari ruangan yang ada pada lantai 2 ini.Biasanya di lobby 
            ini banyak mahasiswa yang berkumpul untuk melihat informasi penting yang diberikan oleh jurusan maupun UKM yang ada di Polinema. 
            Adapun informasi penting dari jurusan yang biasanya ditempelkan pada mading adalah seperti nilai semester, lowongan pekerjaan dari 
            JPC Polinema, hasil Dialog Dosen dan Mahasiswa, dan juga beberapa informasi menarik tentang dunia IT.
            </li>
