            <h2><li>Lab. Basis Data</h2>
            Dengan adanya laboratorium ini, tentunya mahasiswa tidak hanya kuat dari segi teori saja, 
            namun juga dari sisi praktek lapangan. Salah satu laboratorium yang disediakan oleh 
            Politeknik Negeri Malang yaitu LBD 1. LBD 1 ini difungsikan sebagai ruang praktek bagi 
            mahasiswa Politeknik Negeri Malang. Letak dari laboratorium ini terbilang strategis yaitu 
            disebelah kamar mandi lantai 1 Gedung AF. Uniknya lagi pada semester genap lab ini hanya 
            dipakai untuk matakuliah Basis Data saja.
            </li>
            <h2><li>Lab. Studio Proyek</h2>
            Dengan adanya laboratorium ini, tentunya mahasiswa tidak hanya kuat dari segi teori saja, 
            namun juga dari sisi praktek lapangan. Pengaktualisasian berbagai materi tentunya tidak 
            hanya bagus dari sisi akademik, melainkan juga sangat efektif dalam melatih kedisiplinan 
            dan juga kreatifitas. Nah salah satu laboratorium yang disediakan oleh Politeknik Negeri 
            Malang yaitu LPJ. LPJ ini difungsikan sebagai ruang praktek bagi mahasiswa Politeknik 
            Negeri Malang, untuk mengerjakan suatu proyek pengembangan perangkat lunak. Di jurusan TI, 
            terdapat 4 LPJ (LPJ-1, LPJ-2, LPJ-3, LPJ-4) yang bisa digunakan oleh mahasiswa untuk mengembangkan proyek.
            </li>
            <h2><li>Lab. Pemograman</h2>
                Laboratorium ini digunakan untuk menggembleng mahasiswa jurusan TI pada kompetensi 
                pemrograman komputer. Terdapat 6 ruang LPR yang ada di jurusan TI. Laboratorium yang 
                terletak di Gedung Sipil lantai 7 ini, masing-masing memiliki kapasitas 32 orang yang 
                dilengkapi dengan PC di setiap mejanya. Untuk menunjang kegiatan perkuliahan, laboratorium 
                ini memiliki 1 buah LCD dan 1 buah proyektor agar presentasi baik dari dosen maupun mahasiswa 
                dapat berjalan dengan efektif.
            </li>
            <h2><li>Lab. Image dan Graphics</h2>
                Laboratorium Images dan Graphics merupakan salah satu laboratorium yang digunakan mahasiswa 
                Jurusan Teknologi Informasi. Didalam laboratorium tersebut terdapat 30 Personal Computer (PC) 
                yang nantinya dapat digunakan oleh mahasiswa dalam mengikuti kegiatan belajar mengajar dalam 
                mata kuliah praktikum.Laboratorium Images dan Graphics biasanya digunakan untuk mata kuliah Pengolahan Citra Digital.
            </li>
            <h2><li>Lab. Komputasi dan Jaringan</h2>
                Laboratorium ini dikhususkan untuk praktikum matakuliah di ranah jaringan komputer. Laboratorium 
                yang terletak di Gedung Sipil Lantai 7 ini memiliki kapasitas 32 orang. Untuk menunjang kegiatan 
                perkuliahan, laboratorium ini memiliki 1 buah LCD dan 1 buah proyektor agar presentasi dari dosen 
                bisa lebih jelas dan efektif.
            </li>
            <h2><li>Lab. Informatika Dasar</h2>
                Lab. Informatika Dasar yang dimiliki oleh jurusan TI sebanyak 2 ruang lab. Laboratorium yang 
                terletak di Gedung Sipil Lantai 7 ini memiliki kapasitas 32 orang yang dilengkapi dengan PC 
                disetiap mejanya. Untuk menunjang kegiatan perkuliahan, laboratorium ini memiliki 1 buah LCD 
                dan 1 buah proyektor agar presentasi dari dosen bisa lebih jelas dan efektif.
            </li>
            <h2><li>Lab. Sistem Informasi</h2>
                Di dalam lab ini, tidak diberikan perangkat PC karena para mahasiswa dianjurkan untuk menggunakan 
                laptop pribadi yang dimiliki. Ruangan di-set sedemikian rupa sehingga dapat mudah untuk digunakan 
                kolaborasi bagi mahasiswa dalam pengembangan sistem informasi. Laboratorium yang terletak di Gedung 
                 lantai 1 ini memiliki kapasitas 32 orang. Untuk menunjang kegiatan perkuliahan, laboratorium ini 
                 memiliki 1 buah LCD dan 1 buah proyektor agar presentasi dari dosen bisa lebih jelas dan efektif.
            </li>
