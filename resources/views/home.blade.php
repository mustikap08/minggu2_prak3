        <h1 align=center>Selamat Datang di Halaman Utama Jurusan Teknologi Informasi Politeknik Negeri Malang</h1>
        <p>
            Jurusan Teknologi Informasi merupakan salah satu jurusan di Politeknik Negeri Malang, berdasarkan keputusan 
            Direkur Politeknik Negeri Malang nomor 53 tahun 2015 yang terdiri dari Program Studi Diploma III Manajemen 
            Informatika dan Program Studi Diploma IV Teknik Informatika. <br><br>

            Pelaksanaan pendidikannya mengacu pada UU No. 20 tahun 2003 tentang Sistem Pendidikan Nasional dan diwujudkan
            dengan menerapkan kurikulum berbasis kompetensi (KBK) yang sesuai dengan peraturan Kemendiknas No. 045/U/tahun 
            2002. Untuk mewujudkan tuntutan kebutuhan tersebut, Jurusan Teknologi Informasi telah melaksanakan kerjasama 
            dengan berbagai pihak, baik dalam maupun luar negeri sebagai perwujudan visi, misi, dan tujuan.
        </p>
        <h2> Visi Jurusan Teknologi Informasi Polinema </h2>
        <p>
            Jurusan Teknologi Informasi Polinema sebagai pusat unggulan di bidang teknologi informasi 
            dan rekayasa perangkat lunak di tingkat nasional maupun internasional.
        </p>
        <h2> Misi Jurusan Teknologi Informasi Polinema </h2>
        <ol>
            <li>Melaksanakan pendidikan vokasi yang inovatif berdasarkan pada sistem pendidikan terapan dengan memanfaatkan kemajuan teknologi informasi dan telekomunikasi, sehingga mampu menghasilkan lulusan yang siap kerja dengan daya saing global.</li>
            <li>Melaksanakan penelitian terapan berbasis produk dan jasa bidang informatika.</li>
            <li>Melaksanakan pengabdian masyarakat dengan menggunakan kemajuan teknologi informasi untuk meningkatkan kesejahteraan. </li>
            <li>Terwujudnya kerjasama yang saling menguntungkan dengan berbagai pihak baik di dalam maupun di luar negeri pada bidang teknologi informasi. </li>
        </ol>
