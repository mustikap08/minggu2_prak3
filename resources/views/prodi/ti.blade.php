        <h1 align=center>Program Studi Teknik Informatika</h1>
        <p>
            Pada tahun 2010 berdasar kebutuhan masyarakat dan industri terkait Program Diploma IV bidang informatika 
            maka Polinema mendirikan program studi baru Diploma IV (DIV) Teknik Informatika (TI). Pada awal berdirinya 
            jumlah peserta didik Program D-IV TI hanya terdiri dari 46 Mahasiswa, namun pada tahun 2015 jumlah peserta 
            didik Prodi D-IV TI telah mengalami peningkatan menjadi 545 Mahasiswa. Perkembangan jumlah mahasiswa yang 
            sangat pesat baik di Program Studi D-III MI maupun D-IV TI mendorong pimpinan di Polinema untuk menyatukan 
            kedua program studi tersebut dalam satu organisasi jurusan baru terpisah dari jurusan Teknik Elektro dibawa 
            Jurusan baru terpisah dari Jurusan Teknik Elektro. Dan pada tahun 2015 berdasarkan SK Direktur Nomor 53 
            dalam rangka peningkatan mutu pengelolaan dan optimasi sumber daya dibentuklah Jurusan Teknologi Informasi 
            (JTI) dengan prodi D-III MI dan D-IV TI.
        </p>
        <h2> VISI </h2>
        <p>
            Menjadi program studi yang unggul dalam bidang rekayasa perangkat 
            lunak baik di tingkat nasional maupun internasional. 
        </p>
        <h2> MISI </h2>
        <ol>
            <li>Melaksanakan pendidikan vokasi yang inovatif berdasarkan pada sistem pendidikan terapan dengan memanfaatkan kemajuan teknologi, sehingga mampu menghasilkan lulusan yang memiliki kompetensi di bidang rekayasa perangkat lunak dan siap bersaing di tingkat nasional dan global.</li>
            <li>Menghasilkan penelitian terapan berbasis produk dan jasa bidang rekayasa perangkat lunak.</li>
            <li>Melaksanakan pengabdian masyarakat dengan menggunakan kemajuan rekayasa perangkat lunak untuk meningkatkan kesejahteraan.</li>
            <li>Mewujudkan kerjasama yang saling menguntungkan dengan berbagai pihak baik di dalam maupun di luar negeri pada bidang rekayasa perangkat lunak.</li>
        </ol>
